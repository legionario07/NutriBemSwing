    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:

        capturarDados();

        Endereco endereco = new Endereco();
        endereco = loja.getEndereco();

        loja.setEndereco(CepUtil.buscarCep(loja.getEndereco().getCep()));
        loja.getEndereco().setNumero(txtNumero.getText());
        loja.getEndereco().setComplemento(txtComplemento.getText().toUpperCase());
        loja.getEndereco().setId(endereco.getId());

        resultado = new Resultado();
        resultado = fachada.update(loja);

        if (resultado.getEntidades().isEmpty()) {
            JOptionPane.showMessageDialog(null, resultado.getMsg(), "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, resultado.getMsg());
        }


    }//GEN-LAST:event_btnEditarActionPerformed

