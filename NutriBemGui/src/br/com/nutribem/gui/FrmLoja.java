    public void preencherDadosNaView() {

        txtId.setText(String.valueOf(loja.getId()));
        txtCnpj.setText(loja.getCnpj());
        txtNome.setText(loja.getNome());
        txtNomeFantasia.setText(loja.getNomeFantasia());

        txtCep.setText(loja.getEndereco().getCep());
        txtLogradouro.setText(loja.getEndereco().getLogradouro());
        txtNumero.setText(loja.getEndereco().getNumero());
        txtComplemento.setText(loja.getEndereco().getComplemento());
        txtBairro.setText(loja.getEndereco().getBairro());
        txtCidade.setText(loja.getEndereco().getCidade().getNome());

        txtTelefoneFixo.setText(loja.getContato().getTelefoneFixo());
        txtTelefoneComercial.setText(loja.getContato().getTelefoneComercial());
        txtCelular.setText(loja.getContato().getCelular());
        txtEmail.setText(loja.getContato().getEmail());

    }

    public void capturarDados() {

        loja.setId(Long.valueOf(txtId.getText()));
        loja.setCnpj(txtCnpj.getText());
        loja.setNome(txtNome.getText().toUpperCase());
        loja.setNomeFantasia(txtNomeFantasia.getText().toUpperCase());

        loja.getEndereco().setCep(txtCep.getText());
        loja.getEndereco().setLogradouro(txtLogradouro.getText().toUpperCase());
        loja.getEndereco().setNumero(txtNumero.getText());
        loja.getEndereco().setComplemento(txtComplemento.getText().toUpperCase());
        loja.getEndereco().getCidade().setNome(txtCidade.getText().toUpperCase());

        loja.getContato().setTelefoneFixo(txtTelefoneFixo.getText());
        loja.getContato().setTelefoneComercial(txtTelefoneComercial.getText());
        loja.getContato().setCelular(txtCelular.getText());
        loja.getContato().setEmail(txtEmail.getText());

    }

