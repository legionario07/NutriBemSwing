    public void preencherTabelas() {

        UnidadeDeMedidaTableModel unidadeDeMedidaTableModel = new UnidadeDeMedidaTableModel(unidadeDeMedidas);
        tabelaUnidadesDeMedidas = GuiUtil.configurarTabelas(tabelaUnidadesDeMedidas);
        tabelaUnidadesDeMedidas.setModel(unidadeDeMedidaTableModel);
        TableRowSorter<UnidadeDeMedidaTableModel> sorter = new TableRowSorter<UnidadeDeMedidaTableModel>(unidadeDeMedidaTableModel);
        tabelaUnidadesDeMedidas.setRowSorter(sorter);

    }

