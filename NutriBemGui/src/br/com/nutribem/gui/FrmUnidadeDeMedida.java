    public void inicializarCampoPesquisa() {
        txtLocaliza.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                TableUtil.filterTable((TableRowSorter<CategoriaTableModel>) tabelaUnidadesDeMedidas.getRowSorter(), txtLocaliza.getText());
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                TableUtil.filterTable((TableRowSorter<CategoriaTableModel>) tabelaUnidadesDeMedidas.getRowSorter(), txtLocaliza.getText());
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                TableUtil.filterTable((TableRowSorter<CategoriaTableModel>) tabelaUnidadesDeMedidas.getRowSorter(), txtLocaliza.getText());
            }
        });
    }

